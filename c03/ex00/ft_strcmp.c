int	ft_strcmp(char *s1, char *s2)
{
	int	sum;

	sum = 0;
	while (*s1 >= 0)
	{
		while (*s2 >= 0)
		{
			sum = *s1 - *s2;
			if (sum == 0 && (*s1 + *s2) != 0)
			{
				s2++;
				s1++;
			}
			else
				return (sum);
		}
	}
	return (sum);
}
